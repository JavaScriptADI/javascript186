// assignment operators
// null and undefined represent empty values in JS
let age; // undefined

console.log(age);

// assignment 
age = 5;
console.log(age);

// Shorthand addition syntax
// age = age + 1;
age += 1;
console.log(age);

age -= 1;
console.log(age);

age *= 2;
console.log(age);

// age = age / 2;
age /= 2;
console.log(age);
